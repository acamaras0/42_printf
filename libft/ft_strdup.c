#include <stdlib.h>
#include "libft.h"

char	*ft_strdup(const char *src)
{
	char	*dest;
	int		i;
	int		size;

	size = 0;
	while (src[size])
	{
		size++;
	}
	dest = (char *)malloc(sizeof(char) * size + 1);
	if (!(dest))
	{
		return (0);
	}
	i = 0;
	while (i < size)
	{
		dest[i] = src[i];
		i++;
	}
	dest[i] = '\0';
	return (dest);
}
